// Helper function to get random gun by rarity
export function getRandomGun(rarity?: string): GunItem {
  const filtered = rarity 
    ? GUNS.filter(g => g.rarity === rarity)
    : GUNS;
  return filtered[Math.floor(Math.random() * filtered.length)];
}

// Helper function to get gun by ID
export function getGunById(id: string): GunItem | undefined {
  return GUNS.find(g => g.id === id);
}