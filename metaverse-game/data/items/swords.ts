// Helper function to get random sword by rarity
export function getRandomSword(rarity?: string): SwordItem {
  const filtered = rarity 
    ? SWORDS.filter(s => s.rarity === rarity)
    : SWORDS;
  return filtered[Math.floor(Math.random() * filtered.length)];
}

// Helper function to get sword by ID
export function getSwordById(id: string): SwordItem | undefined {
  return SWORDS.find(s => s.id === id);
}